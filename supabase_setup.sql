SELECT name_en, crop, saved, is_verified
FROM public.community_stories
ORDER BY created_at DESC;

-- Check 4: Grand total (should be 17+ rows in daily_alerts)
SELECT
    COUNT(*) AS total_alert_rows,
    COUNT(DISTINCT commodity) AS crops,
    COUNT(DISTINCT district) AS districts
FROM public.daily_alerts;
