-- ============================================================
-- STEP 1A: Create / patch daily_alerts table
-- (adds missing columns rise_score & trend_is_rising)
-- ============================================================

CREATE TABLE IF NOT EXISTS public.daily_alerts (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    date          DATE NOT NULL,
    commodity     TEXT NOT NULL,
    district      TEXT NOT NULL,
    price         NUMERIC NOT NULL DEFAULT 0,
    min_price     NUMERIC NOT NULL DEFAULT 0,
    max_price     NUMERIC NOT NULL DEFAULT 0,
    crash_score   NUMERIC NOT NULL DEFAULT 0.5,
    rise_score    NUMERIC NOT NULL DEFAULT 0.0,
    trend_is_rising BOOLEAN NOT NULL DEFAULT false,
    alert_level   TEXT NOT NULL DEFAULT 'AMBER',
    message       TEXT NOT NULL DEFAULT '',
    created_at    TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    UNIQUE(date, commodity, district)
);

-- Add new columns if they don't exist yet (safe to re-run)
ALTER TABLE public.daily_alerts
    ADD COLUMN IF NOT EXISTS rise_score      NUMERIC NOT NULL DEFAULT 0.0,
    ADD COLUMN IF NOT EXISTS trend_is_rising BOOLEAN NOT NULL DEFAULT false,
    ADD COLUMN IF NOT EXISTS min_price       NUMERIC NOT NULL DEFAULT 0,
    ADD COLUMN IF NOT EXISTS max_price       NUMERIC NOT NULL DEFAULT 0;

-- Create pipeline_runs table
CREATE TABLE IF NOT EXISTS public.pipeline_runs (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_date   TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    status     TEXT NOT NULL,
    logs       TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- RLS: allow anonymous read (backend uses SERVICE_ROLE key for writes)
ALTER TABLE public.daily_alerts   ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.pipeline_runs  ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Enable read access for all users" ON public.daily_alerts;
CREATE POLICY "Enable read access for all users"
    ON public.daily_alerts AS PERMISSIVE FOR SELECT TO public USING (true);


-- ============================================================
-- STEP 1B: Create community_stories table
-- ============================================================

CREATE TABLE IF NOT EXISTS public.community_stories (
    id           SERIAL PRIMARY KEY,
    initials     TEXT NOT NULL,
    name_en      TEXT NOT NULL,
    avatar_color TEXT NOT NULL DEFAULT 'green',
    distance_km  TEXT NOT NULL DEFAULT '0 km away',
    message_mr   TEXT NOT NULL,
    message_en   TEXT NOT NULL,
    is_verified  BOOLEAN NOT NULL DEFAULT true,
    verified_date TEXT,
    crop         TEXT NOT NULL DEFAULT 'Soybean',
    saved        TEXT NOT NULL DEFAULT '₹0',
    created_at   TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

ALTER TABLE public.community_stories ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Enable read for community stories" ON public.community_stories;
CREATE POLICY "Enable read for community stories"
    ON public.community_stories AS PERMISSIVE FOR SELECT TO public USING (true);


